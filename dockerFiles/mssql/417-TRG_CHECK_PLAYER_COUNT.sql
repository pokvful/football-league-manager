CREATE OR ALTER TRIGGER TRG_CHECK_PLAYER_COUNT
	ON LINEUP
	FOR INSERT, UPDATE
	AS
BEGIN
	IF @@ROWCOUNT > 0
		BEGIN TRY
			IF EXISTS (
				SELECT L.MATCH_ID, COUNT(L.PLAYER_PERSON_ID) AS player_count
				FROM lINEUP AS L
				INNER JOIN PLAYER AS P
					ON P.PERSON_ID = L.PLAYER_PERSON_ID
				WHERE L.MATCH_ID IN (
					SELECT MATCH_ID
					FROM INSERTED
				)
				GROUP BY L.MATCH_ID, P.CLUB_NAME
				HAVING COUNT(L.PLAYER_PERSON_ID) < 7 OR COUNT(L.PLAYER_PERSON_ID) > 11
			)
			THROW 50002, 'There can only be a minimum of 7 and a maximum of 11 players in a club while playing a match', 1;
		END TRY

		BEGIN CATCH
			THROW
		END CATCH
END

GO

